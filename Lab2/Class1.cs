    public class Avg
    {
        //Average of 2 Nums
        public static double aver(double x, double y)
        {
            return (x+y) / 2;
        }
        //Average of 3 nums
        public static double aver(double x, double y, double z)
        {
            return (x+y+ z) / 3;
        }
    }
